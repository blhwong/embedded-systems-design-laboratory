//*************RIT128x96x4_Line********************************************
// Draws one line in the RAM version of the image
// Inputs: (x1,y1) is the start point
// (x2,y2) is the end point
// color is 0 (off) to 15 (white)
// coordinates range from 0 to MAX, 0 is bottom or left, MAX is top or right
// Outputs: none

void RIT128x96x4_Line(int x1, int y1, int x2, int y2, unsigned char color)
{
	
	int sx,sy, dx, dy, err, e2;
	int i =1;
	xpos = x1;
	ypos = y1;
	height = y2-y1;
	width = x2-x1;
	color = color;
	////
	
	dx = abs(x2-x1);
	dy = abs(y2-y1);
	if(x1<x2){
		sx = 1;
	}
	else
	{
		sx = -1;
	}
	if(y1<y2)
	{
		sy = 1;
	}
	else
	{
		sy = -1;
	}
	err = dx - dy;
	while(i==1){
		PLOT[y1][x1] = color;
		if((x1 == x2) && (y1 == y2))
		{
				break;
		}
		e2 = 2*err;
		if(e2 > -dy)
		{
			err = err - dy;
			x1 = x1 + sx;
		}
		if((x1 == x2) && (y1 == y2))
		{
			PLOT[y1][x1]= color;
			break;
		}
		if(e2<dx)
		{
			err = err + dx;
			y1 = y1 + sy;
		}
	}
	
}
